// Topic 搜索状态
let topicSearchQuery = '';
let topicSearchFilter = 'all'; // 'all' | 'author' | 'team'

function filterTopics(topics) {
  const q = topicSearchQuery.trim().toLowerCase();
  if (!q) return topics;
  return topics.filter(t => {
    if (topicSearchFilter === 'author') {
      return t.author.toLowerCase().includes(q);
    }
    if (topicSearchFilter === 'team') {
      return t.team.toLowerCase().includes(q);
    }
    // 'all' — 模糊搜索：标题、发布人、团队、事件子标题、事件正文、AI 小结
    const fields = [
      t.title, t.author, t.team,
      ...(t.events || []).flatMap(e => [e.subtitle || '', e.text || '', e.aiSummary || ''])
    ];
    return fields.some(f => f.toLowerCase().includes(q));
  });
}

function onTopicSearchInput(value) {
  topicSearchQuery = value;
  renderSSVer();
}

function setTopicSearchFilter(filter) {
  topicSearchFilter = filter;
  // 如果有搜索词则重新过滤
  if (topicSearchQuery.trim()) {
    renderSSVer();
  }
}

function clearTopicSearch() {
  topicSearchQuery = '';
  topicSearchFilter = 'all';
  renderSSVer();
}

