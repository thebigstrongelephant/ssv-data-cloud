function nexusAiAnswer(query) {
  const q = query.toLowerCase();

  // 收集所有 topics（含 project topics + 上墙 topics）
  const allTopics = [...WALL_TOPICS, ...TOPICS_DATA];
  Object.values(PROJECT_TOPICS).forEach(arr => { allTopics.push(...arr); });

  // 0. 关于"上墙"的专项提问
  if (hasKeyword(q, ['上墙','置顶','全员通知','全员关注','全员必看','重要通知'])) {
    return buildWallAnswer();
  }

  // 1. 关于特定 Topic / 项目的提问
  const topicMatch = findRelevantTopics(q, allTopics);
  if (topicMatch.length > 0 && (hasKeyword(q, ['进展','进度','怎么样','怎样','情况','状态','最新','详情','更新','汇报','成果']))) {
    return buildTopicProgressAnswer(q, topicMatch);
  }

  // 2. 关于特定人的提问
  if (hasKeyword(q, ['suri','张明','李哲','赵宇','刘芳','陈琳','孙健','吴强','周洋','王佳','钱峰','李晨'])) {
    return buildPersonAnswer(q, allTopics);
  }

  // 3. 关于团队/实验室的提问
  const teamMatch = findTeamFromQuery(q);
  if (teamMatch && hasKeyword(q, ['okr','目标','krs','完成','进度','情况','怎么样','计划','规划'])) {
    return buildTeamOkrAnswer(teamMatch);
  }

  // 4. 风险相关
  if (hasKeyword(q, ['风险','问题','挑战','阻塞','延迟','困难','risk','at-risk'])) {
    return buildRiskAnswer(q, allTopics);
  }

  // 5. @ / 待办 / todo 相关
  if (hasKeyword(q, ['待办','todo','ai todo','行动项','跟进','任务'])) {
    return buildTodoAnswer(q, allTopics);
  }

  // 6. 组织架构 / 团队概况
  if (hasKeyword(q, ['组织','架构','团队','部门','实验室','有哪些','多少','总览','overview'])) {
    return buildOrgOverviewAnswer();
  }

  // 7. OKR 全局概况
  if (hasKeyword(q, ['okr','目标','关键结果','完成率','进度'])) {
    return buildAllOkrAnswer();
  }

  // 8. 宪章相关
  if (hasKeyword(q, ['宪章','charter','数据治理','合规','隐私','审计'])) {
    return buildCharterAnswer(allTopics);
  }

  // 9. 通用 topic 搜索
  if (topicMatch.length > 0) {
    return buildTopicProgressAnswer(q, topicMatch);
  }

  // 10. Fallback：尝试全文搜索
  const searchResults = fullTextSearch(q, allTopics);
  if (searchResults.length > 0) {
    return buildSearchAnswer(q, searchResults);
  }

  // 无法回答
  return {
    answer: '抱歉，我暂时没有找到与你问题直接相关的信息。你可以试试：<br>• 询问具体的 Topic 或项目（如"景德镇项目进展"）<br>• 询问特定团队的 OKR（如"教育实验室目标"）<br>• 询问特定人员的相关事项（如"Suri 相关的 Topic"）<br>• 询问组织架构概况',
    sources: []
  };
}

// ---- 工具函数 ----

function hasKeyword(q, keywords) {
  return keywords.some(k => q.includes(k.toLowerCase()));
}

function findRelevantTopics(q, allTopics) {
  const results = [];
  allTopics.forEach(t => {
    let score = 0;
    const title = (t.title || '').toLowerCase();
    const words = q.replace(/[？?！!。，,、]/g, ' ').split(/\s+/).filter(w => w.length > 1);
    words.forEach(w => { if (title.includes(w)) score += 10; });

    // 搜索事件内容
    (t.events || []).forEach(evt => {
      const sub = (evt.subtitle || '').toLowerCase();
      const text = (evt.text || '').toLowerCase();
      const summary = (evt.aiSummary || '').toLowerCase();
      words.forEach(w => {
        if (sub.includes(w)) score += 5;
        if (text.includes(w)) score += 3;
        if (summary.includes(w)) score += 2;
      });
    });

    if (score > 0) results.push({ topic: t, score });
  });
  results.sort((a, b) => b.score - a.score);
  return results.slice(0, 5);
}

