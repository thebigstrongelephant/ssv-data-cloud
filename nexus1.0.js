function openDetail(type, id) {
  currentView = 'detail';
  // For plugins, add data to global skills
  if (type === 'skill' && window._pluginData) {
    const plugin = window._pluginData.find(p => p.id === id);
    if (plugin && !SSV_SKILLS.find(s => s.id === id)) {
      SSV_SKILLS.push(plugin);
    }
  }
  detailContext = { type, id };
  render();
  mainArea.scrollTop = 0;
}

