function publishNewEvent(topicId) {
  const t = TOPICS_DATA.find(x => x.id === topicId);
  if (!t) return;
  if (!newEventState.subtitle.trim()) { alert('请输入子标题'); return; }
  const now = new Date().toISOString().slice(0,16).replace('T',' ');
  const evtIdx = t.events.length + 1;
  const lastEvt = t.events[t.events.length - 1];
  // AI 自动生成小结和待办（模拟）
  const autoSummary = '【AI 自动生成】' + newEventState.subtitle + '。' + (newEventState.text ? newEventState.text.substring(0, 100) + (newEventState.text.length > 100 ? '...' : '') : '暂无正文内容。') + (newEventState.files.length ? ' 已上传 ' + newEventState.files.length + ' 份文件。' : '');
  const autoTodos = newEventState.text ? ['基于最新更新信息，跟进相关行动项', '确认本次更新对现有计划的影响'] : [];

  const newEvt = {
    id: 'e_' + topicId + '_' + evtIdx,
    subtitle: newEventState.subtitle,
    time: now,
    visibility: newEventState.visibility || (lastEvt ? lastEvt.visibility : 'SSV 全员'),
    mentions: newEventState.mentions || '',
    files: newEventState.files.map(f => ({name: f, type: f.split('.').pop()})),
    text: newEventState.text,
    comments: [],
    aiSummary: autoSummary,
    aiTodos: autoTodos
  };
  t.events.push(newEvt);

  // 更新 topic 时间
  t.time = now;

  // 重置状态
  addingNewEvent = false;
  newEventState = { subtitle: '', text: '', files: [], visibility: '', mentions: '' };

  // 展开新事件，收拢其他
  expandedEvents = { _initialized: true, _topicId: t.id };
  t.events.forEach((e, i) => {
    expandedEvents[e.id] = (i === t.events.length - 1);
  });
  activeEventId = newEvt.id;

  renderTopicDetail();
}

