    const summary = latestEvt.aiSummary.replace(/<br\s*\/?>/gi, '\n').replace(/<[^>]+>/g, '');
    const short = summary.length > 500 ? summary.substring(0, 500) + '…' : summary;
    answer += short.replace(/\n/g, '<br>');
    sources.push(`Topic「${top.title}」· 事件「${latestEvt.subtitle}」`);
