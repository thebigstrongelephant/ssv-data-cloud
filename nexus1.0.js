function saveEditTopic(topicId) {
  const t = TOPICS_DATA.find(x => x.id === topicId);
  if (!t) return;

  const titleInput = document.getElementById('editTopicTitle');
  const visInput = document.getElementById('editTopicVisibility');
  const mentionsInput = document.getElementById('editTopicMentions');
  const coMaintainersInput = document.getElementById('editCoMaintainers');
  const checkboxes = document.querySelectorAll('#linkTeamList input[type=checkbox]:checked');

  // 更新标题
  if (titleInput && titleInput.value.trim()) {
    t.title = titleInput.value.trim();
  }

  // 更新共同维护人
  if (coMaintainersInput) {
    const raw = coMaintainersInput.value.trim();
    t.coMaintainers = raw ? raw.split(/[、,，]/).map(s => s.trim()).filter(Boolean) : [];
  }

  // 更新可见范围（写入第一个事件和 topic 级别）
  if (visInput) {
    t.visibility = visInput.value.trim();
    if (t.events && t.events[0]) {
      t.events[0].visibility = visInput.value.trim();
    }
  }

  // 更新 @（写入第一个事件）
  if (mentionsInput) {
    if (t.events && t.events[0]) {
      t.events[0].mentions = mentionsInput.value.trim();
    }
  }

  // 更新关联组织
  const newLinkedTeams = Array.from(checkboxes).map(cb => cb.value);
  const oldLinkedTeams = t.linkedTeams || [];
  t.linkedTeams = newLinkedTeams;

  // 将 Topic 挂载到对应组织的 PROJECTS 列表中
  // 移除旧关联（不在新列表中的）
  oldLinkedTeams.forEach(teamKey => {
    if (!newLinkedTeams.includes(teamKey)) {
      removeTopicFromProjects(topicId, teamKey);
    }
  });

  // 添加新关联
  newLinkedTeams.forEach(teamKey => {
    if (!oldLinkedTeams.includes(teamKey)) {
      addTopicToProjects(t, teamKey);
    }
  });

  // 关闭弹窗并刷新
  const modal = document.getElementById('editTopicModal');
  if (modal) modal.remove();
  renderTopicDetail();
}

function addTopicToProjects(topic, teamKey) {
  if (!PROJECTS[teamKey]) PROJECTS[teamKey] = [];

  // 检查是否已存在
  const existId = 'linked_' + topic.id + '_' + teamKey;
  if (PROJECTS[teamKey].find(p => p.id === existId)) return;

  // 创建关联项目条目
  const linkedProject = {
    id: existId,
    name: topic.title,
    status: 'on-track',
    statusText: '正常',
    desc: '关联自 Topic：' + topic.title,
    pct: 0,
    color: TEAMS[teamKey]?.color || 'var(--blue)',
    owner: topic.author,
    aiSource: 'Topic 关联',
    isLinkedTopic: true,
    sourceTopicId: topic.id,
    detail: {
      position: topic.title,
      goal: '',
      timeline: topic.events ? topic.events.map(e => ({
        date: e.time, text: e.subtitle, source: 'Topic 事件'
      })) : [],
      aiNote: '此项目由 Topic 关联而来'
    }
  };

  PROJECTS[teamKey].push(linkedProject);

  // 同步创建 PROJECT_TOPICS 条目
  PROJECT_TOPICS[existId] = {
    id: 'pt_' + existId,
    title: topic.title,
    author: topic.author,
    team: TEAMS[teamKey]?.name || '',
    time: topic.time,
    visibility: TEAMS[teamKey]?.name + ' 全员',
    isProjectTopic: true,
    projectId: existId,
    teamKey: teamKey,
    sourceTopicId: topic.id,
    events: topic.events || []
  };
}

function removeTopicFromProjects(topicId, teamKey) {
  if (!PROJECTS[teamKey]) return;
  const existId = 'linked_' + topicId + '_' + teamKey;
  const idx = PROJECTS[teamKey].findIndex(p => p.id === existId);
  if (idx >= 0) {
    PROJECTS[teamKey].splice(idx, 1);
  }
  // 清理 PROJECT_TOPICS
  delete PROJECT_TOPICS[existId];
}

